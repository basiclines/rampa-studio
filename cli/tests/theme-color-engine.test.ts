import { deriveEditorPalette, type EditorPalette } from '../src/theme-color-engine';
import { hexToOklch } from '../../src/engine/OklchMathEngine';

// ─── helpers ────────────────────────────────────────────────────────────────

/** Normalise hex to lowercase 7-char (#rrggbb). */
function norm(hex: string): string {
  return hex.toLowerCase().slice(0, 7);
}

/**
 * Assert two hex colours are within `maxDeltaL` on the OKLCH L axis.
 * L is in [0, 1]; maxDeltaL = 0.05 means ±5 % perceptual lightness.
 */
function expectClose(got: string, want: string, maxDeltaL = 0.05, label = '') {
  const [lg] = hexToOklch(norm(got));
  const [lw] = hexToOklch(norm(want));
  const delta = Math.abs(lg - lw);
  expect(delta).toBeLessThanOrEqual(
    maxDeltaL,
    `${label}: got ${got} (L=${lg.toFixed(3)}) vs want ${want} (L=${lw.toFixed(3)}), ΔL=${delta.toFixed(3)} > ${maxDeltaL}`,
  );
}

function loadTheme(filename: string) {
  const yaml = readFileSync(join(import.meta.dir, '../../themes', filename), 'utf8');
  return parseThemeYAML(yaml);
}

