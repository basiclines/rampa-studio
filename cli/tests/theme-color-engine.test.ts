// ─── Aura Dark quality anchors (verified against native xcode / alacritty) ──
// These are the real syntax-token colors from the official Aura Theme repo.
// They serve as *quality* assertions, tighter than the regression tolerance.
const AURA_QUALITY: Partial<Record<keyof EditorPalette, string>> = {
  keyword: '#8464C6', // purple — confirmed in aura-theme xcode + alacritty
  string:  '#54C59F', // muted green — confirmed in Aura xcode syntax keys
  fn:      '#C7A06F', // warm gold   — confirmed in Aura xcode syntax keys
  type:    '#6CB2C7', // teal        — confirmed in xcode DVT syntax keys
};

// ─── tests ──────────────────────────────────────────────────────────────────

describe('deriveEditorPalette', () => {
  for (const fixture of FIXTURES) {
    describe(fixture.label, () => {
      const theme  = loadTheme(fixture.file);
      const palette = deriveEditorPalette(theme);

      // ── mode ──────────────────────────────────────────────────────────────
      it('detects correct mode', () => {
        expect(palette.mode).toBe(fixture.mode);
      });

      // ── passthrough ───────────────────────────────────────────────────────
      it('bg passes through unchanged', () => {
        expect(norm(palette.bg)).toBe(norm(fixture.expected.bg));
      });

      it('fg passes through unchanged', () => {
        expect(norm(palette.fg)).toBe(norm(fixture.expected.fg));
      });

      // ── regression (±5 % OKLCH lightness) ───────────────────────────────
      const derived = [
        'lineHighlight', 'selection', 'comment',
        'keyword', 'string', 'fn', 'type', 'cursor',
      ] as const;

      for (const key of derived) {
        it(`${key} within 5% OKLCH lightness of baseline`, () => {
          expectClose(palette[key], fixture.expected[key], 0.05, key);
        });
      }

      // ── structural invariants ────────────────────────────────────────────
      it('lineHighlight is darker than selection', () => {
        const [lLH] = hexToOklch(palette.lineHighlight);
        const [lSel] = hexToOklch(palette.selection);
        if (fixture.mode === 'dark') {
          expect(lLH).toBeLessThan(lSel);
        } else {
          expect(lLH).toBeGreaterThan(lSel);
        }
      });

      it('keyword and string are different colors', () => {
        expect(norm(palette.keyword)).not.toBe(norm(palette.string));
      });

      it('returns all required palette keys', () => {
        const required: (keyof EditorPalette)[] = [
          'bg', 'bgAlt', 'lineHighlight', 'selection', 'selectionBorder',
          'fg', 'fgMuted', 'comment', 'cursor',
          'keyword', 'string', 'number', 'fn', 'type', 'variable', 'operator',
          'added', 'removed', 'modified',
          'error', 'warning', 'info', 'mode',
        ];
        for (const key of required) {
          expect(palette[key]).toBeDefined();
        }
      });
    });
  }

  // ── quality anchors against real Aura syntax values ───────────────────────
  describe('Aura Dark quality anchors', () => {
    const theme   = loadTheme('aura-theme--aura-dark.yaml');
    const palette = deriveEditorPalette(theme);

    for (const [key, real] of Object.entries(AURA_QUALITY) as [keyof EditorPalette, string][]) {
      it(`${key} within 3% of real Aura value (${real})`, () => {
        expectClose(palette[key] as string, real, 0.03, `aura.${key}`);
      });
    }
  });
});
