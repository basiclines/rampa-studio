/**
 * Baseline tests for deriveEditorPalette().
 *
 * Two layers of assertions per theme:
 *
 * 1. PASSTHROUGH — bg and fg must be identical to what the YAML contains
 *    (no derivation involved; these are taken straight from theme.colors).
 *
 * 2. REGRESSION — every derived semantic color is compared to the expected
 *    value captured when the engine was first calibrated. A tolerance of
 *    ±5 % OKLCH-lightness is allowed so minor tuning doesn't break tests,
 *    but large algorithm regressions are caught immediately.
 *
 * To re-calibrate baselines (e.g. after intentional engine changes), run:
 *   bun scripts/gen-fixtures.ts > /dev/null  # update manually
 */
import { describe, it, expect } from 'bun:test';
import { readFileSync } from 'fs';
import { join } from 'path';
